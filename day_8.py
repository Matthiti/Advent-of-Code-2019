WIDTH = 25  # Not needed, since I already split the input into lines of 25
HEIGHT = 6


def get_layers() -> [str]:
	layers = []
	input_split = input.split('\n')

	i = 0
	while i < len(input_split):
		layers.append(''.join(input_split[i:i + HEIGHT]))
		i += HEIGHT
	return layers


def get_layer_with_least_zeros() -> str:
	layers = get_layers()

	min = -1
	min_layer = None

	for l in layers:
		count = l.count('0')
		if count < min or min == -1:
			min = count
			min_layer = l
	return min_layer


def calculate() -> int:
	layer = get_layer_with_least_zeros()
	amount_1 = layer.count('1')
	amount_2 = layer.count('2')
	return amount_1 * amount_2



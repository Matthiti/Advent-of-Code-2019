from enum import Enum


class Color(Enum):
	BLACK = '0'
	WHITE = '1'
	TRANSPARANT = '2'

	def __str__(self) -> str:
		if self == Color.BLACK:
			return u"\u25A0"
		elif self == Color.WHITE:
			return u"\u25A1"
		else:
			return ""

	def __repr__(self) -> str:
		return self.__str__()


def decode_image() -> [Color]:
	layers = get_layers()
	result = []
	for i in range(WIDTH * HEIGHT):
		for l in layers:
			color = Color(l[i])
			if color == Color.BLACK or color == Color.WHITE:
				result.append(color)
				break
	return result


def main() -> str:
	img = decode_image()
	res = ""
	for i in range(len(img)):
		if i % WIDTH == 0:
			res += "\n"
		res += str(img[i])
	print(res)


